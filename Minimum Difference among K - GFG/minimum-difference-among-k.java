class Solution
{
    long minDiff(long a[] ,int N,int K)
    {
    long res=Long.MAX_VALUE;
    Arrays.sort(a);
    int i=0; int j=K-1;
    while(i<N && j<N){
        res=Math.min(res, (a[j]-a[i]));
        i++;j++;
    }
    return res;
        
    }
}


