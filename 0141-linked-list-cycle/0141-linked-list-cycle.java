/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public boolean hasCycle(ListNode head) {
        //In this question we will use the floyd's cycle detection algorithm
        //we will take two pointers fast and slow and while they are not null we will move fast pointer by 2 step and slow pointer by 1 step
        //If at any place both the fast and slow pointer meet then our linkedlist contains a cycle.
        ListNode slow=head;
        ListNode fast=head;
        while(fast!=null && fast.next!=null){
            slow=slow.next;
            fast=fast.next.next;
            if(slow==fast){
                return true;
            }
        }
        return false;
    }
}